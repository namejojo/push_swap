#include "../libft.h"

int	ft_putnbr(int nb)
{
	int	len;

	len = 1;
	if (-10 < nb && nb < 0)
		len += write (1, "-", 1);
	if (nb <= -10 || nb >= 10)
		len += ft_putnbr(nb / 10);
	if (nb < 0)
		nb = -(nb % 10) + '0';
	else
		nb = (nb % 10) + '0';
	write (1, &nb, 1);
	return (len);
}
